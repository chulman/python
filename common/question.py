# 1시간은 몇초?

# 계산한 값을 seconds_per_hours
seconds_per_hours = 60 * 60
print(seconds_per_hours)
# 1일은?
seconds_per_day = seconds_per_hours * 24
print(seconds_per_day)

# / seconds_per_day를 seconds_per_hour로 나누어라
print(seconds_per_day/seconds_per_hours)

# // 나눗셈을 사용하여 sec_p d 를 sec_per_hour

print(seconds_per_day//seconds_per_hours)
